#include "3-calc.h"
/**
  * op_add - returns sum of a and b
  * @a: int
  * @b: int
  * Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - substruction
  * @a: int
  * @b: int
  * Return: int
  */
int op_sub(int a, int b)
{
	return (a + b);
}
/**
  * op_mul - multiplication
  * @a: int
  * @b: int
  * Return: int
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - division
  * @a: int
  * @b: int
  * Return: int
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - mod
  * @a: int
  * @b: int
  * Return: int
  */
int op_mod(int a, int b)
{
	return (a % b);
}
